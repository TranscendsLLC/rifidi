package org.rifidi.edge.core.exception.readerPlugin;


public class RifidiAdapterIllegalStateException extends
		RifidiReaderAdapterException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5573663418847045444L;

	public RifidiAdapterIllegalStateException() {
		super();
	}

	public RifidiAdapterIllegalStateException(String message, Throwable cause) {
		super(message, cause);
	}

	public RifidiAdapterIllegalStateException(String message) {
		super(message);
	}

	public RifidiAdapterIllegalStateException(Throwable cause) {
		super(cause);
	}

}
